package dtos;

public class MovieData {

    private String titleMovie;
    private String scoreMovie;
    private String yearMovie;

    public String getTitleMovie() {
        return titleMovie;
    }

    public void setTitleMovie(String titleMovie) {
        this.titleMovie = titleMovie;
    }

    public String getScoreMovie() {
        return scoreMovie;
    }

    public void setScoreMovie(String scoreMovie) {
        this.scoreMovie = scoreMovie;
    }

    public String getYearMovie() {
        return yearMovie;
    }

    public void setYearMovie(String yearMovie) {
        this.yearMovie = yearMovie;
    }
}
